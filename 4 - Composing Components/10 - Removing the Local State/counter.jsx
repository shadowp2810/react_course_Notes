/*
We want to remove the local state 
and only rely on the props 
to recieve the local data this component needs.
We refer to this kind of component as a controlled component.
A controlled component does not have it's own local state,
it recieve all it's data via props 
and raises events whenever data needs to be changed.
So this component is entirely controlled by its parent.
Now we rely entirely on props object.
*/

class Counter extends React.Component {
  render() {
    return (
      <div>
        <span className={this.getBadgeClasses()}>{this.formatCount()}</span>
        <button
          onClick={() => this.props.onIncrement(this.props.counter)}
          className="btn btn-secondary btn-sm"
        >
          Increment
        </button>
        <button
          onClick={() => this.props.onDelete(this.props.counter.id)}
          className="btn btn-danger btn-sm m-2"
        >
          Delete
        </button>
      </div>
    );
  }

  getBadgeClasses() {
    let classes = "badge m-2 badge-";
    classes += this.props.counter.value === 0 ? "warning" : "primary";
    return classes;
  }

  formatCount() {
    const { value: count } = this.props.counter;
    return count === 0 ? "Zero" : count;
  }
}

export default Counter;
